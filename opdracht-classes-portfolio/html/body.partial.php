                    De Animaris Rhonoceros Transport weegt 2,8 ton en is een zijtak in de strandbeesten evolutie maar kan desondanks door één enkele persoon worden voortbewogen door slim gebruik te maken van de wind. Je kunt er in zitten. Zoals een auto een vervoertuigd paard is en een vliegtuig gezien kan worden als een vervoertuigde vogel, zo kan de Animaris Rhinoceros Transport opgevat worden als een vervoertuigde Animaris (de Latijnse naam voor strandbeest).
                </p>
                
                <p>
                    De Animaris Rhinoceros Transport die het stadsdeel Nieuw-West in de collectie heeft, is ontworpen door de kunstenaar maar de technische ontwikkeling en realisatie van dit lopende kunstwerk is in 2004 gedaan door het bedrijf Heijmerink | Wagemakers, Nieuwegein.
                </p>
                
                <p>
                    Het ontwerp waarmee kunstenaar Theo Jansen bij Heijmerink|Wagemakers kwam, was een kartonnen schaalmodel van 20 cm hoog waarin de unieke beweging van de Animaris al lag opgesloten. Het werkelijke beest is 4,5 meter hoog en 6 meter breed.
                </p>
                
                <p>
                    De Animaris Rhinoceros Transport stond lange tijd in de Albardagracht (in de wijk Geuzenveld-Slotermeer), een plek waar hij niet tot zijn recht kwam. Afgelopen jaren heeft het stadsdeel de deze Animaris laten restaureren en is samen met de kunstenaar gezocht naar een betere plek in het stadsdeel. Dat is Business Park Amsterdam Osdorp (BPAO) geworden. Het kunstwerk wordt in de entreezone in een speciale kooi geplaatst (mei 2014). Het is de bedoeling dat ‘het beest’ zo nu en dan los gaat.
